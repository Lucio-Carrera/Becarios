create table Instituciones(
ID_Institucion int auto_increment not null,
Institucion varchar (200) not null,
primary key (ID_Institucion)
);


create table Organismos(
ID_Org int auto_increment not null,
Organismo varchar (50) not null,
primary key (ID_Org)
);

CREATE TABLE departamentos (
  ID_Org int(11) not null primary key auto_increment,
  Departamentos varchar(200) not null
);

create table DatosArea(
ID_DatosArea int auto_increment not null,
ID_Org3 varchar (150) not null,
ID_Dir3 varchar (150) not null,
ID_Sub3 varchar (150) not null,
ID_Ger3 varchar (150) not null,
Nombre_Asesor varchar (100) not null,
CargoAsesor varchar (150) not null,
