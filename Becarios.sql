F_Final date not null,
Hr_Inicio time not null,
Hr_Final time not null,
Proyecto varchar (1000) not null,
F_Convenio date not null,
Nombre_Gerente varchar (100) not null,
CargoGerente varchar (150) not null,
F_InicioRen date not null,
F_FinalRen date not null,
F_RenConvenio date not null,
ID_Estudiante3 int not null,
Ano_Reg decimal (4) not null,
primary key (ID_DatosArea),
foreign key (ID_Estudiante3, Ano_Reg) references DatosPersonales (ID_Est, Ano_Reg)
);

create table OficioSalida (
No_Oficio int auto_increment not null,
Ano decimal (4) not null,
Nombre_Oficio_Dirigido varchar (150) not null,
Asunto varchar (30) not null,
